#  floor division
#  divides the first number by the second number, but returns the integer part of the result
print(num1 // num2)
#  ==================================================================
#  exponentiation
#  raises the first number to the power of the second number
print(num1 ** num2)

#  ==================================================================
#  equal to
#  returns True if the two numbers are equal
print(num1 == num2)
